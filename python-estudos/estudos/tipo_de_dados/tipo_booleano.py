# Negação NOT
"""
    Fazendo a negação, se o valor booleano for verdadeiro o resultado será False, se 
    o resultado for true retornara True
"""
print(not True)

# ou (OR)
"""
    É uma operação binária, ou seja, dependendo dos valores um ou o outro será verdadeiro
    
    Exemplo:
        True or True -> True // True or False -> True // False or False -> False
"""
print(True or True)

